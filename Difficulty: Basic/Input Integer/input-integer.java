import java.util.Scanner;

class GFG {
    public static void main(String args[]) {
        // Create Scanner object to read input
        Scanner sc = new Scanner(System.in);
        
        // Take integer input from user
        int n = sc.nextInt();
        
        // Add 10 to the input
        int result = n + 10;
        
        // Print the result
        System.out.println(result);
        
        // Close the scanner
        sc.close();
    }
}
